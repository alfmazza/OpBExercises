public class CIf {

    public static void main(String[] args) {

        int numeroIf = 0;

        if (numeroIf < 0) {
            System.out.println("El número es negativo");
        } else if (numeroIf > 0) {
            System.out.println("El número es positivo");
        } else {
            System.out.println("El número es 0");
        }
    }
}